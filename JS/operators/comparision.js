// Comparision Operator

// Equality ==
// Not Equal !=
// Greater than >
// Greater than equal >=
// Less than <
// Less than equal <=
// Strict Equality ===
// Strict Not Equal !==


var x = 9;
var y = 10;

// Equality

console.log(x == y);

// Not Equal
console.log(x != y);

// Greater than

console.log(x > y);

// Greater than equal

console.log(x >= y);

// Less than

console.log(x < y)

// Less than equals to
console.log(x <= y);


// Strict Equality 

var a = 10;

var b = '10';

console.log(a === b)


console.log(a !== b);
