// Arithmetic Operators

// Addition +
// Substraction -
// Multiplication
// Divide /
// Modulus %
// Increment ++
// Decrement --
// Unary Plus +
// Unary Negation -

// Addition
var addition = 10 + 20;
console.log(addition);

// Substraction

var substract = 20-60;
console.log(substract);

// Multiplicaton

var mul = 10 * 2;
console.log(mul);

// Division

var divide = 55 / 10;
console.log(divide);


// modulus

var mod = 55 % 10;
console.log(mod);


// post increment

var x = 10;

console.log(x++);
console.log(x);

x++;

console.log('post incre',x);

// pre increment

var y = 10;
console.log(++y)
++y;

console.log('pre incre ',y);

// Decrement Operator

var a = 20;

// post decrement
console.log(a--)

// pre decrement
var b = 20
console.log(--b);


// Unary Plus

var c = +'3';


var bool = +true;

console.log(c);

console.log(bool);


// Unary Negation

var d = -'-3';

console.log(d);